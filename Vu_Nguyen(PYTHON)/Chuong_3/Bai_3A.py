# Toán tử/Phép toán (Operator) là 1 ký hiệu/câu lệnh cho phép tính toán gì đó dựa trên giá trị của các biến để tính ra được 1 kết quả nào đó. Ví dụ: Dấu + là phép cộng 2 số
# Trong 1 phép toán cần có sự tham gia của các biến, hoặc giá trị, và các thành phần này được gọi chung là toán hạng (Operand). Ví dụ: Phép cộng cần có 2 toán hạng tham gia
# Sự kết hợp giữa toán tử và toán hạng được gọi là biểu thức (expression). Ví dụ: x + y là 1 biểu thức gồm toán tử + và 2 toán hạng x,y


