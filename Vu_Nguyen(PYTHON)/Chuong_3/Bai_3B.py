# Lý thuyết 03.B - Phép gán (Assignment operator)
# Phép gán (dấu = ) là 1 phép toán 2 toán hạng thể hiện việc láy dữ liệu từ toán hạng bên phải và gán sang toán hạng bên trái.
# 1 phép toán có 5 đặc điểm cần lưu ý:
# + Biểu thị của phép toán: = 
# + Số toán hạng tham gia:
# + Sự ưu tiên của toán hạng trong biểu thức: Thấp nhất
# + Thứ tự thực hiện: từu phải sang trái
# + Giá trị trả về: 

x = y = z = 10
x = 10 
y = 100
print(x + 1)